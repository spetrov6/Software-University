n = int(input())
for row in range(1, n + 1):
    if row == 1 or row == n:
        print("*" * (2 * n) + " " * n + "*" * (2 * n))
    elif n % 2 == 0:
        if row == (n / 2):
            print("*" + "/" * (2*n-2) + "*" + "|" * n + "*" + "/" * (2*n-2) + "*")
        else:
            print("*" + "/" * (2*n-2) + "*" + " " * n + "*" + "/" * (2*n-2) + "*")
    else:
        if row == (n - 1) // 2 + 1:
            print("*" + "/" * (2*n-2) + "*" + "|" * n + "*" + "/" * (2*n-2) + "*")
        else:
            print("*" + "/" * (2*n-2) + "*" + " " * n + "*" + "/" * (2*n-2) + "*")